// Operador de Concatenación
// El operador de concatenación (+) se utiliza para unir dos o más cadenas de texto.

// Concatenación de cadenas de texto
let nombre = "Juan";
let apellido = "Pérez";
let nombreCompleto = nombre + " " + apellido; // Juan Pérez

// Concatenación de cadenas de texto con números
// Si se concatena una cadena de texto con un número, el número se convierte en cadena de texto.
let edad = 22;
let texto = "Mi edad es " + edad; // Mi edad es 22