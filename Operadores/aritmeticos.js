// Operadores aritméticos
// Suma
let a = 5;
let b = 2;
let suma = a + b; // 7

// Resta
let resta = a - b; // 3

// Multiplicación
let multiplicacion = a * b; // 10

// División
let division = a / b; // 2.5

// Módulo
let modulo = a % b; // 1

// Exponenciación
let exponenciacion = a ** b; // 25

// Incremento  
let incremento = a++; // 5

// Decremento
let decremento = a--; // 5