// Operadores de asignación
// Los operadores de asignación se utilizan para asignar valores a las variables.

// = Asignación
let x = 5; // x = 5

// Asginación de adición
let y = 10;
y += 5; // y = y + 5 = 15

// Asignación de resta
let z = 20;
z -= 5; // z = z - 5 = 15

// Asignación de multiplicación
let a = 10;
a *= 5; // a = a * 5 = 50

// Asignación de división
let b = 20;
b /= 2; // b = b / 2 = 10